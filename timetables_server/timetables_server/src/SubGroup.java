import java.io.Serializable;

public class SubGroup
        implements Serializable {
    private String name;
    private int index;

    public SubGroup(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getIndex() {
        return this.index;
    }

    public void setIndex(int index) {
        this.index = index;
    }
}